export function getStaticTranslation(key: string, category: string, targetLang: string): string | null {
  const langTranslations = STATIC_TRANSLATIONS[targetLang];
  if (!langTranslations) return null;
  
  const categoryTranslations = langTranslations[category];
  if (!categoryTranslations) return null;
  
  return categoryTranslations[key] || null;
}

export function getAllStaticCategories(): string[] {
  const categories: string[] = [];
  for (const lang of Object.values(STATIC_TRANSLATIONS)) {
    for (const category of Object.keys(lang)) {
      if (!categories.includes(category)) {
        categories.push(category);
      }
    }
  }
  return categories;
}
