export type Language = 'pl' | 'en' | 'de' | 'cz' | 'es' | 'ua';

export function getTranslation(lang: Language | undefined | null, key: string): string {
  if (!key || typeof key !== 'string') return '';
  const keys = key.split('.');
  let value: any = translations;
  
  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k];
    } else {
      return key;
    }
  }
  
  if (typeof value === 'object' && value !== null) {
    return value[lang || 'pl'] || value['pl'] || Object.values(value)[0] || key;
  }
  
  return typeof value === 'string' ? value : key;
}